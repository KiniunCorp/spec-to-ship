import { existsSync, mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import type { AppScaffoldOptions, AppScaffoldResult } from '../types/index.js';
import { commandExists, runShell } from './shell.js';

export function scaffoldAppRepository(options: AppScaffoldOptions): AppScaffoldResult {
  const appName = normalizeName(options.appName || 'my-app');
  const appRepoPath = path.resolve(options.appRepoPath);
  const worktreesRootPath = path.resolve(options.worktreesRootPath);
  const overwrite = Boolean(options.overwrite);

  mkdirSync(appRepoPath, { recursive: true });
  mkdirSync(worktreesRootPath, { recursive: true });

  const createdFiles: string[] = [];
  const skippedExistingFiles: string[] = [];

  const initializedGit = ensureLocalGitRepo(appRepoPath);

  const files =
    options.mode === 'recommended'
      ? buildRecommendedTemplate(appName, worktreesRootPath)
      : buildCustomTemplate(appName, worktreesRootPath, options.customStackNotes);

  for (const [relative, content] of Object.entries(files)) {
    const fullPath = path.join(appRepoPath, relative);
    mkdirSync(path.dirname(fullPath), { recursive: true });
    if (existsSync(fullPath) && !overwrite) {
      skippedExistingFiles.push(fullPath);
      continue;
    }
    writeFileSync(fullPath, content, 'utf8');
    createdFiles.push(fullPath);
  }

  return {
    appName,
    mode: options.mode,
    appRepoPath,
    worktreesRootPath,
    initializedGit,
    createdFiles,
    skippedExistingFiles,
  };
}

function ensureLocalGitRepo(appRepoPath: string): boolean {
  if (!commandExists('git', appRepoPath)) {
    throw new Error('git is required but not available in PATH.');
  }
  const check = runShell('git', ['rev-parse', '--is-inside-work-tree'], appRepoPath, true);
  if (check.status === 0) return false;

  const initMain = runShell('git', ['init', '-b', 'main'], appRepoPath, true);
  if (initMain.status !== 0) {
    runShell('git', ['init'], appRepoPath);
    runShell('git', ['checkout', '-B', 'main'], appRepoPath, true);
  }
  return true;
}

