function buildCustomTemplate(appName: string, worktreesRootPath: string, notes?: string): Record<string, string> {
  return {
    '.gitignore': ['node_modules/', 'dist/', '.env*', '.DS_Store', ''].join('\n'),
    'README.md': [
      `# ${appName}`,
      '',
      'Custom app scaffold generated by SpecToShip.',
      '',
      'This mode lets you bring your own stack while preserving SpecToShip execution contracts.',
      notes ? '' : '',
      notes ? `Custom stack notes: ${notes}` : '',
      '',
    ]
      .filter(Boolean)
      .join('\n'),
    'package.json': JSON.stringify(
      {
        name: appName,
        version: '0.1.0',
        private: true,
        scripts: {
          lint: 'node -e "console.log(\'configure lint for your stack\')"',
          test: 'node -e "console.log(\'configure tests for your stack\')"',
          build: 'node -e "console.log(\'configure build for your stack\')"',
          verify: 'npm run lint && npm run test && npm run build',
        },
      },
      null,
      2,
    ) + '\n',
    'config/workspace.paths.json': JSON.stringify(
      {
        project_dir_name: appName,
        workdir_dir_name: `${appName}-workdir`,
        worktrees_root: worktreesRootPath,
      },
      null,
      2,
    ) + '\n',
    'scripts/create-change-worktree.sh': createChangeWorktreeScript(),
    'justfile': createAppJustfile(),
    'openspec/changes/README.md': '# Open changes for this app\n',
    'openspec/specs/README.md': '# Stable specs for this app\n',
  };
}

function createAppJustfile(): string {
  return [
    'set shell := ["bash", "-eu", "-o", "pipefail", "-c"]',
    '',
    'default:',
    '  @just --list',
    '',
    'change-worktree change_id provider="codex":',
    '  bash scripts/create-change-worktree.sh {{change_id}} {{provider}}',
    '',
    'agent-verify:',
    '  npm run verify',
    '',
    'dev:',
    '  npm run dev',
    '',
  ].join('\n');
}

function createChangeWorktreeScript(): string {
  return [
    '#!/usr/bin/env bash',
    'set -euo pipefail',
    '',
    'change_id="${1:-}"',
    'provider="${2:-codex}"',
    'provider="$(echo "$provider" | tr "[:upper:]" "[:lower:]" | sed -E "s/[^a-z0-9]+/-/g; s/^-+|-+$//g")"',
    'if [[ -z "$change_id" ]]; then',
    '  echo "Usage: bash scripts/create-change-worktree.sh <change-id> [provider]"',
    '  exit 1',
    'fi',
    'if [[ -z "$provider" ]]; then',
    '  provider="custom"',
    'fi',
    '',
    'repo_root="$(git rev-parse --show-toplevel)"',
    'branch="s2s-${provider}/${change_id}"',
    '',
    'cfg_path="$repo_root/config/workspace.paths.json"',
    'worktrees_root=""',
    'if [[ -f "$cfg_path" ]]; then',
    '  worktrees_root="$(node -e "const fs=require(\'fs\');const p=JSON.parse(fs.readFileSync(process.argv[1],\'utf8\'));process.stdout.write(String(p.worktrees_root||\'\'));" "$cfg_path")"',
    'fi',
    'if [[ -z "$worktrees_root" ]]; then',
    '  repo_name="$(basename "$repo_root")"',
    '  worktrees_root="$(cd "$repo_root/.." && pwd)/${repo_name}-worktrees"',
    'fi',
    '',
    'mkdir -p "$worktrees_root"',
    'worktree_path="$worktrees_root/$change_id"',
    '',
    'if ! git show-ref --verify --quiet "refs/heads/$branch"; then',
    '  git branch "$branch" >/dev/null',
    'fi',
    '',
    'if [[ -d "$worktree_path/.git" || -f "$worktree_path/.git" ]]; then',
    '  echo "path: $worktree_path"',
    '  exit 0',
    'fi',
    '',
    'git worktree add "$worktree_path" "$branch" >/dev/null',
    'echo "path: $worktree_path"',
    '',
  ].join('\n');
}

function normalizeName(value: string): string {
  const normalized = String(value || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
  return normalized || 'my-app';
}
